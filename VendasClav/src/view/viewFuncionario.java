    private void btnPesqAlterFunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesqAlterFunActionPerformed
        // TODO add your handling code here:
       
        
        int pesquisar = Integer.parseInt(txtPesqAlter.getText());
        FuncionarioDAO fDAO = new FuncionarioDAO();
        // Aqui criei uma variavel do tipo modelFuncionario;
        modelFuncionario k = fDAO.getFuncionario(pesquisar);
                
        if(k == null)
        {
            JOptionPane.showMessageDialog(this, "Funcionario não encontrado");
            
        }
        else
        {
            // Preencher o campos do form com os dados do retorno;
            txtIdFun.setText(String.valueOf(k.getId_funcionario()));
            txtNomeFun.setText(k.getNomeFun());
            txtTelefoneFun.setText(String.valueOf(k.getTelefoneFun()));
            txtMoradaFun.setText(k.getMorada());
            txtNumeroBI.setText(k.getnBI());
            txtEditData.setText(k.getDataNascimento());
            txtUsuarioLog.setText(k.getUsuarioLogin());
            txtSenhaLog.setText(k.getSenhaLogin());
            if(k.getCargoFun().trim().equals("Administrador"))
             {
                rbAdministrador.setSelected(true);
             }
             else{
                rbAdministrador.setSelected(false);
                rbComum.setSelected(true);
             }
        
            if(k.getSexo().trim().equals("Masculino"))
             {
               rbMasculino.setSelected(true);
             }
            else{
               rbMasculino.setSelected(false);
               rbFemenino.setSelected(true);
             }
            
        }
        
    }//GEN-LAST:event_btnPesqAlterFunActionPerformed

