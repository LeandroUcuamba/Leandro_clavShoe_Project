    private void btnGuardarFunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarFunActionPerformed
        // TODO add your handling code here:
        
        String Ccargo;
        String Ssexo;
        
        if(rbAdministrador.isSelected())
        {
            Ccargo = "Administrador";
        }
        else{
            Ccargo = "Comum";
        }
        
        
        if(rbMasculino.isSelected())
        {
            Ssexo = "Masculino";
        }
        else{
            Ssexo = "Femenino";
        }
        
        modelFuncionario p = new modelFuncionario();
        FuncionarioDAO dao = new FuncionarioDAO();
        
        p.setNomeFun(txtNomeFun.getText());
        p.setTelefoneFun(Integer.parseInt(txtTelefoneFun.getText()));
        p.setMorada(txtMoradaFun.getText());
        p.setCargoFun(Ccargo);
        p.setDataNascimento(txtEditData.getText());
        p.setSexo(Ssexo);
        p.setnBI(txtNumeroBI.getText());
        p.setUsuarioLogin(txtUsuarioLog.getText());
        p.setSenhaLogin(txtSenhaLog.getText());
        p.setMorada(txtMoradaFun.getText());
        p.setNifLoja(txtNifLoja.getText());
        dao.create(p);
        
        this.setVisible(false);
        this.dispose();
        new viewFuncionario1().setVisible(true);
        
        // Limpar campos apois o cadastro de funcionario;
        rbAdministrador.setSelected(false);
        rbComum.setSelected(false);
        rbFemenino.setSelected(false);
        rbMasculino.setSelected(false);
        txtEditData.setText("");
        txtMoradaFun.setText("");
        txtNifLoja.setText("");
        txtNumeroBI.setText("");
        txtNomeFun.setText("");
        txtTelefoneFun.setText("");
        txtUsuarioLog.setText("");
        txtSenhaLog.setText("");
        
        new viewFuncionario1().readJTable1();
    }//GEN-LAST:event_btnGuardarFunActionPerformed

