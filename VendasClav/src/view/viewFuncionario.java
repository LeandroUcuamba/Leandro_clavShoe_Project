        p.setTelefoneFun(Integer.parseInt(txtTelefoneFun.getText()));
        p.setMorada(txtMoradaFun.getText());
        p.setCargoFun(Ccargo);
        p.setDataNascimento(txtEditData.getText());
        p.setSexo(Ssexo);
        p.setnBI(txtNumeroBI.getText());
        p.setUsuarioLogin(txtUsuarioLog.getText());
        p.setSenhaLogin(txtSenhaLog.getText());
        p.setMorada(txtMoradaFun.getText());
        p.setNifLoja(txtNifLoja.getText());
        dao.update(p);
        
        // Limpar campos apois o cadastro de funcionario;
        txtIdFun.setText("");
        rbAdministrador.setSelected(false);
        rbComum.setSelected(false);
        rbFemenino.setSelected(false);
        rbMasculino.setSelected(false);
        txtEditData.setText("");
        txtMoradaFun.setText("");
        txtNifLoja.setText("");
        txtNumeroBI.setText("");
        txtNomeFun.setText("");
        txtTelefoneFun.setText("");
        txtUsuarioLog.setText("");
        txtSenhaLog.setText("");
        
        new viewFuncionario1().readJTable1();
        
        
