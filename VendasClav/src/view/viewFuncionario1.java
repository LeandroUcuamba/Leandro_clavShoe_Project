    private void btnRemoverFunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverFunActionPerformed
        // TODO add your handling code here:
        
        // getSeletedRow() -> linha selecionada;
        if (jtFuncionario.getSelectedRow() != -1) {
             
            modelFuncionario p = new modelFuncionario();
            FuncionarioDAO dao = new FuncionarioDAO();
            
            p.setId_funcionario((int)jtFuncionario.getValueAt(jtFuncionario.getSelectedRow(), 0));
            
            dao.delete(p);

            // codigo para limpar campo apois o registro;
        rbAdministrador.setSelected(false);
        rbComum.setSelected(false);
        rbFemenino.setSelected(false);
        rbMasculino.setSelected(false);
        txtMoradaFun.setText("");
        txtNifLoja.setText("");
        txtNumeroBI.setText("");
        txtNomeFun.setText("");
        txtTelefoneFun.setText("");
        txtUsuarioLog.setText("");
        txtSenhaLog.setText("");
            //fecho da limpa nos campos
            readJTable1();
        }
        else{
            JOptionPane.showMessageDialog(null, "selecciona um cliente para excluir");
        }
        
    }//GEN-LAST:event_btnRemoverFunActionPerformed

