    public void readJTable1() {
        DefaultTableModel modelo = (DefaultTableModel) jtFuncionario.getModel();
        modelo.setNumRows(0); // para quando apresentar na JTable apois um cadastro não duplicar valores;

        FuncionarioDAO cliDao = new FuncionarioDAO();

        // este é o ForEach;
        for (modelFuncionario pesqFun : cliDao.read1()) {
            modelo.addRow(new Object[]{
                pesqFun.getId_funcionario(),
                pesqFun.getNomeFun(),
                pesqFun.getTelefoneFun(),
                pesqFun.getCargoFun(),
                pesqFun.getDataNascimento(),
                pesqFun.getMorada(),
                pesqFun.getSexo(),
                pesqFun.getnBI(),
                pesqFun.getUsuarioLogin(),
                pesqFun.getSenhaLogin()
            });
        }
    }
    
